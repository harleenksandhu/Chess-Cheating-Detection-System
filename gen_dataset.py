# Script that generates cheating games by injecting bot moves at varying intensities
from stockfish import Stockfish
import random
import chess
import chess.pgn
from datasets import load_dataset
import csv
import io
import multiprocessing as mp
import pandas as pd
import glob

# Global defines
TOTAL_GAMES = 50000
CORES = 8
SF_PATH = r"bin\stockfish\stockfish-windows-x86-64-avx2.exe"

'''
Translates SAN format to UCI list for stockfish to process
'''
def get_uci_moves(move_text):
    try:
        pgn = io.StringIO(move_text)
        game = chess.pgn.read_game(pgn)
        if game:
            return [move.uci() for move in game.mainline_moves()]
        else:
            return []
    except:
        return []
    

'''
Simulates thinking time for engine and human moves
'''
def get_simulated_time(is_engine_move, game_phase):
    if is_engine_move:
        return round(random.uniform(3.5, 6.0), 2)
    else:
        # human behaviour typically changes depending on the phase of the game
        # so to simulate the time it would take for a human to make a move, the timing changes depending on the phase
        if game_phase == "opening":
            return round(random.uniform(0.5, 2.5), 2) # fast, book moves
        elif game_phase == "midgame":
            return round(random.lognormvariate(2.0, 0.7), 2) # log-normal distribution (most are about 5s, some are 40+)
        else: 
            return round(random.uniform(1.0, 8.0), 2) # endgame timing

'''
Takes a list of moves (human_moves) and injects engine moves 
based on the intensity
'''
def inject_stockfish_moves(human_moves, sf, white_elo, black_elo, intensity = 0.2):
    # Array to keep track of which moves were generated by the engine
    move_log = [] 

    # virtual chess board to track piece counts
    board = chess.Board() 

    for i, move in enumerate(human_moves):
        if board.is_game_over(): break

        # sf.make_moves_from_start(cheating_sequence) # set current position for engine
        sf.set_fen_position(board.fen())
        eval_before = sf.get_evaluation() # get evaluation before move (for CPL calculation)

        piece_count = len(board.piece_map())

        if i <= 10:
            game_phase = "opening"
        elif piece_count <= 14 or i >= 40: 
            game_phase = "endgame"
        else:
            game_phase = "midgame"

        # identify who is moving
        if board.turn == chess.WHITE:
            player_elo = white_elo
            opponent_elo = black_elo
        else: 
            player_elo = black_elo
            opponent_elo = white_elo

        elo_diff = player_elo - opponent_elo

        # decide to cheat or keep human move
        if random.random() < intensity:
            move_to_play = sf.get_best_move()
            move_type = "engine"
        else:
            # Checking if the human move is still legal on our modified board
            try:
                move_obj = chess.Move.from_uci(move)
                if move_obj in board.legal_moves:
                    move_to_play = move
                    move_type = "human"
                else:
                    # If illegal, engine must take over to keep the game going
                    move_to_play = sf.get_best_move()
                    move_type = "engine"
            except:
                move_to_play = sf.get_best_move()
                move_type = "engine"
        
        if move_to_play is None: break # No more moves
        
        board.push_uci(move_to_play) # update board

        # Calculating centipawn loss (for ACPL)
        sf.set_fen_position(board.fen())
        eval_after = sf.get_evaluation()
        
        # Simple CP Loss calculation normalized for player turn
        if eval_before['type'] == 'cp': 
            v1 = eval_before['value']
        else:
            v1 = 1000 if eval_before['value'] > 0 else -1000

        if eval_after['type'] == 'cp': 
            v2 = eval_after['value']
        else:
            v2 = 1000 if eval_after['value'] > 0 else -1000

        if board.turn == chess.WHITE:
            raw_cp_loss = v2 - v1
        else:
            raw_cp_loss = v1 - v2

        cp_loss = max(0, raw_cp_loss)
        cp_loss = min(cp_loss, 400) # capping cp loss at 400 

        move_time = get_simulated_time(move_type == "engine", game_phase)
        move_log.append({"move_index": i, 
                         "type": move_type, 
                         "player_elo": player_elo, 
                         "opponent_elo": opponent_elo, 
                         "elo_diff": elo_diff, 
                         "move": move_to_play, 
                         "move_time": move_time, 
                         "phase": game_phase, 
                         "cp_loss": cp_loss})


    return move_log

'''
Generates the dataset by injecting stockfish engine moves
'''
def generate_dataset(num_games = 1000, worker_id = 0, start_index = 0):
    # Initializing stockfish engine
    try:
        local_sf = Stockfish(path = SF_PATH, parameters={"Threads": 1, "Hash": 256})
        local_sf.set_depth(10)
    except Exception as e:
        print(f"Worker {worker_id} has failed to start Stockfish: {e}")
        return

    # Load dataset
    ds = load_dataset("Lichess/standard-chess-games", split="train", streaming=True)
    ds_iter = iter(ds.skip(start_index)) 

    output_file = f"temp_worker_{worker_id}.csv"

    with open(output_file, "w", newline="") as file:
        fieldnames = ["game_id", "is_cheater", "player_elo", "opponent_elo", "elo_diff", "move_index", "move", "move_time", "phase", "cp_loss", "label"]
        writer = csv.DictWriter(file, fieldnames=fieldnames)
        writer.writeheader()

        game_count = 0
        for i in range(num_games):
            try: 
                entry = next(ds_iter)
                
                white_elo = entry.get("WhiteElo", 1500)
                black_elo = entry.get("BlackElo", 1500)

                # Converting the 'movetext' column from the HF dataset into a UCI list
                human_moves = get_uci_moves(entry['movetext'])
                if not human_moves: continue

                # Determine if the game should be a cheater game (goal is to have roughly 50% cheating games in the dataset)
                if random.random() < 0.5:
                    intensity = random.choice([0.1, 0.2, 0.4, 0.6])
                    is_cheater = 1
                else: 
                    intensity = 0.0 # fully human game
                    is_cheater = 0

                # Run simulation for injecting cheating moves and creating move log
                move_log = inject_stockfish_moves(human_moves, local_sf, white_elo, black_elo, intensity)

                for move_data in move_log:
                    writer.writerow({
                        "game_id": f"W{worker_id}_G{game_count}",
                        "is_cheater": is_cheater, 
                        "player_elo": move_data["player_elo"], 
                        "opponent_elo": move_data["opponent_elo"], 
                        "elo_diff": move_data["elo_diff"],
                        "move_index": move_data["move_index"], 
                        "move": move_data["move"],
                        "move_time": move_data["move_time"],
                        "phase": move_data["phase"],
                        "cp_loss": move_data["cp_loss"],
                        "label": 1 if move_data["type"] == "engine" else 0 
                    })       
                
                game_count += 1
                if i % 20 == 0:
                    print(f"Progress: {game_count}/{num_games} games generated.")
            except:
                continue
    return output_file

'''
Calls generate_dataset for a single worker task
'''
def worker_task(worker_id, games_per_worker):
    start_index = worker_id * games_per_worker
    generate_dataset(num_games = games_per_worker, worker_id=worker_id, start_index=start_index)

'''
Merges all temp files into one
'''
def merge_worker_files():
    # Grab all files starting with 'temp_worker_'
    all_files = glob.glob("temp_worker_*.csv")
    if not all_files: return
    
    # Combine them into one dataframe
    df_list = [pd.read_csv(f) for f in all_files]
    final_df = pd.concat(df_list, ignore_index=True)
    
    # Save the final product
    final_df.to_csv(f"final_training_data_{TOTAL_GAMES}.csv", index=False)
    print(f"Successfully merged {len(all_files)} files into final_training_data.csv")

if __name__ == "__main__":
    games_per_core = TOTAL_GAMES // CORES

    with mp.Pool(CORES) as pool:
        pool.starmap(worker_task, [(i, games_per_core) for i in range(CORES)])

    merge_worker_files()


